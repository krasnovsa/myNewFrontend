function normalizePath(path) {
  return path.replace(/^\\/, "./").replace(/\\/g, "/");
}

function buildHierarchy(data) {
  // Normalize paths and initialize children arrays for folders
  data.forEach((item) => {
    item.relPath = normalizePath(item.relPath);
    if (item.isFolder === 1) {
      item.children = [];
    }
  });

  // Create a map for easy lookup based on relPath
  const itemMap = {};
  data.forEach((item) => {
    itemMap[item.relPath] = item;
  });

  // Array to hold the root-level items
  const result = [];

  // Build the hierarchy
  data.forEach((item) => {
    if (item.relPath === "./") {
      // Root-level items (those whose relPath is './')
      result.push(item);
    } else {
      // Calculate the parent path by stripping the last folder/file from relPath
      const parentPath = item.relPath.substring(
        0,
        item.relPath.lastIndexOf("/")
      );

      // Lookup the parent in the itemMap
      const parent = itemMap[parentPath];

      if (parent && parent.isFolder === 1) {
        // Add this item to its parent's children
        parent.children.push(item);
      }
    }
  });

  return result;
}

